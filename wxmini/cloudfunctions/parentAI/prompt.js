function buildPrompt(scene, category, userInput, childContext) {
  let userMessage = `【场景分类】${category}
【具体场景】${scene}
【家长描述】${userInput}`;

  if (childContext) {
    userMessage += `\n\n${childContext}`;
  }

  userMessage += `\n\n请分析这个亲子冲突场景，给出结构化的解决方案。如果提供了孩子档案，请根据孩子的年龄、性格特征和出生顺序来调整建议——不同年龄阶段对应不同的教育工具，不同性格适合不同的沟通方式。`;

  return { system: SYSTEM_PROMPT, user: userMessage };
}

